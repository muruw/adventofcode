package _2021.day9_smoke_basin;

public class Basin {
  Integer posX;
  Integer posY;

  public Basin(Integer x, Integer y) {
    this.posX = x;
    this.posY = y;
  }

  public Integer getPosX() {
    return posX;
  }

  public void setPosX(Integer posX) {
    this.posX = posX;
  }

  public Integer getPosY() {
    return posY;
  }

  public void setPosY(Integer posY) {
    this.posY = posY;
  }
}
